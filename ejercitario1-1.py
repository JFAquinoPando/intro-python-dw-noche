# Leer los tres lados de un triángulo escaleno. Calcular su perímetro
"""
Leer los tres lados de un triángulo escaleno. Calcular su perímetro
(escaleno = los 3 lados diferentes)
"""
lado1 = int(input("Ingrese el valor para el lado 1: "))
lado2 = int(input("Ingrese el valor para el lado 2: "))
lado3 = int(input("Ingrese el valor para el lado 3: "))

perimetro = lado1 + lado2 + lado3
print("El perimetro del triangulo escaleno es de:", perimetro, "metros")

